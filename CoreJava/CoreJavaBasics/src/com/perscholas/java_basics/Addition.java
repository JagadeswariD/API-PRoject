package com.perscholas.java_basics;

public class Addition {

	public static void main(String[] args) {
		// adding two integers
		int a,b;
		a=3;
		b=4;
		int sum =a+b;
		System.out.println("Sum:"+sum);
		// adding two double
		double ad,bd;
		ad=1.2;
		bd=2.4;
		double add=ad+bd;
		System.out.println("Add:"+add);
		//adding double and integer
		int x=5;
		double y=4.5;
		double result=x+y;
		System.out.println("Adding int and double :"+result);
		
	}

}
